let currentSlide = 0;

function renderSlide() {
    const slide = slides[currentSlide];
    const imgEl = document.getElementById('slide-image');
    const imgContainer = document.querySelector('.slide-image-container');

    if (slide.image) {
        imgEl.src = slide.image;
        imgEl.alt = slide.title;
        imgEl.style.display = "";
        imgContainer.style.display = "";
    } else {
        imgEl.src = "";
        imgEl.alt = "";
        imgEl.style.display = "none";
        imgContainer.style.display = "none";
    }

    document.getElementById('slide-title').textContent = slide.title;
    document.getElementById('slide-description').textContent = slide.description;
    document.getElementById('slide-progress').textContent = slide.progress;
    document.getElementById('slide-num').textContent = `${currentSlide + 1} / ${slides.length}`;

    const listEl = document.getElementById('slide-list');
    listEl.innerHTML = '';
    slide.list.forEach(item => {
        const li = document.createElement('li');
        li.innerHTML = `<span class="slide-icon">&#9679;</span><span class="slide-list-text">${item}</span>`;
        listEl.appendChild(li);
    });
}

function nextSlide() {
    if (currentSlide < slides.length - 1) {
        currentSlide++;
        renderSlide();
    }
}

function prevSlide() {
    if (currentSlide > 0) {
        currentSlide--;
        renderSlide();
    }
}

document.addEventListener('DOMContentLoaded', function () {
    renderSlide();

    document.getElementById('next-slide-btn').addEventListener('click', nextSlide);
    document.getElementById('prev-slide-btn').addEventListener('click', prevSlide);

    document.addEventListener('keydown', function (e) {
        if (e.key === 'ArrowRight') nextSlide();
        if (e.key === 'ArrowLeft') prevSlide();
    });
});
